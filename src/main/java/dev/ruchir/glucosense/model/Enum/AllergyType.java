package dev.ruchir.glucosense.model.Enum;

public enum AllergyType {
    FOOD,                 // Allergies related to food items
    MEDICATION,           // Allergies related to medications
    ENVIRONMENTAL,        // Allergies caused by environmental factors (e.g., pollen, dust)
    INSECT,               // Allergies to insect stings or bites
    ANIMAL,               // Allergies to animals or animal dander
    LATEX,                // Allergies to latex products
    METAL,                // Allergies to metals (e.g., nickel)
    CHEMICAL,             // Allergies to chemicals or substances (e.g., cleaning agents)
    OTHER                 // Any other types of allergies not covered above
}
